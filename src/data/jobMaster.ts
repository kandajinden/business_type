// input_job_bonus の判定
// ──────────────────────────────
// 入力職種とジョブマスタのカテゴリが一致 → +15
// 近接カテゴリ → +8
// それ以外 → 0

const NEAR_CATEGORIES: Record<JobCategory, JobCategory[]> = {
  sales:       ["marketing"],
  marketing:   ["sales", "creative"],
  engineering: ["creative"],
  admin:       [],
  creative:    ["marketing", "engineering"],
  executive:   ["freelance"],
  freelance:   ["executive"],
  other:       [],
};

export function getInputJobBonus(inputJob: JobCategory, masterCategories: JobCategory[]): number {
  if (masterCategories.includes(inputJob)) return 15;
  const nearCats = NEAR_CATEGORIES[inputJob] || [];
  if (masterCategories.some((c) => nearCats.includes(c))) return 8;
  return 0;
}
